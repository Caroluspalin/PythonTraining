# 1  For loop demonstration
# This function prints numbers from 0 to 9 using a for loop

def for_silmukka():
    for i in range(10):
        print(i, end=' ')
        
for_silmukka()


# 2  For loop from 10 to 30
# This function prints numbers from 10 to 30 using a for loop

def tulosta_numerot():
    for i in range(10, 31):
        print(i, end=' ')

tulosta_numerot()
